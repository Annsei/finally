# AKShare provider (cn) — 东财 qfq daily bars, lazy import, 2 retries
# ---------------------------------------------------------------------------


def _import_akshare():
    """Lazy akshare import — module-level hook so tests can monkeypatch."""
    import akshare  # noqa: PLC0415 — deliberate lazy import (contract §1)

    return akshare


class AkshareProvider:
    """A-share daily bars via AKShare's 东财 interface (前复权 qfq).

    Retries ``retries`` times with linear backoff (``backoff_seconds``,
    2x, ...) around the injectable ``fetch_fn``; ``sleep`` is injectable so
    tests never wait. Only the 东财 ``stock_zh_a_hist`` endpoint is used
    (contract §1 — the Sina endpoints are deliberately avoided).
    """

    name = "akshare"

    def __init__(
        self,
        fetch_fn: Callable[[str, date, date], list[DailyBar]] | None = None,
        retries: int = 2,
        backoff_seconds: float = 0.8,
        sleep: Callable[[float], None] = time.sleep,
    ) -> None:
        self._fetch_fn = fetch_fn
        self._retries = retries
        self._backoff_seconds = backoff_seconds
        self._sleep = sleep

    def fetch_daily(self, ticker: str, start: date, end: date) -> list[DailyBar]:
        symbol = ticker.strip().upper()
        fetch = self._fetch_fn or self._default_fetch
        attempts = self._retries + 1
        last_error: str = "unknown error"
        for attempt in range(attempts):
            try:
                bars = fetch(symbol, start, end)
                if not bars:
                    raise HistoryFetchError(
                        f"akshare returned no daily bars for {symbol}"
                    )
                return bars
            except HistoryFetchError as exc:
                last_error = str(exc)
            except Exception as exc:
                last_error = str(exc)
            if attempt < attempts - 1:
                self._sleep(self._backoff_seconds * (attempt + 1))
        raise HistoryFetchError(
            f"akshare fetch for {symbol} failed after {attempts} attempts: "
            f"{last_error}"
        )

    def _default_fetch(self, symbol: str, start: date, end: date) -> list[DailyBar]:
        try:
            akshare = _import_akshare()
        except Exception as exc:
            raise HistoryFetchError(
                "History source 'akshare' is unavailable — install the "
                f"'akshare' package to sync A-share daily bars ({exc})"
            ) from exc
        frame = akshare.stock_zh_a_hist(
            symbol=symbol,
            period="daily",
            start_date=start.strftime("%Y%m%d"),
            end_date=end.strftime("%Y%m%d"),
            adjust="qfq",
        )
        bars: list[DailyBar] = []
        for _, row in frame.iterrows():
            bars.append(
                DailyBar(
                    date=str(row["日期"]),
                    open=float(row["开盘"]),
                    high=float(row["最高"]),
                    low=float(row["最低"]),
                    close=float(row["收盘"]),
                    volume=float(row["成交量"]),
                )
            )
        bars.sort(key=lambda b: b.date)
        return bars


def build_default_providers(market: str) -> dict[str, HistoryProvider]:
    """The standard provider set for one market, keyed by source name.

    Construction is cheap and import-free (the real providers import their
    packages lazily inside ``fetch_daily``), so building this at router
    creation can never fail or block startup (contract §0).
    """
    return {
        "sample": SampleProvider(market),
        "yfinance": YFinanceProvider(),
        "akshare": AkshareProvider(),
    }


# ---------------------------------------------------------------------------
# SQLite storage — daily_bars upserts and reads
# ---------------------------------------------------------------------------


def upsert_daily_bars(
    conn,
    *,
    market: str,
    ticker: str,
    bars: list[DailyBar],
    source: str,
    fetched_at: str | None = None,
) -> int:
    """INSERT OR REPLACE one ticker's bars. Does NOT commit; returns count."""
    stamp = fetched_at or datetime.now(timezone.utc).isoformat()
    conn.executemany(
        "INSERT OR REPLACE INTO daily_bars "
        "(market, ticker, date, open, high, low, close, volume, source, fetched_at) "
        "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)",
        [
            (
                market,
                ticker,
                bar.date,
                float(bar.open),
                float(bar.high),
                float(bar.low),
                float(bar.close),
                float(bar.volume),
                source,
                stamp,
            )
            for bar in bars
        ],
    )
    return len(bars)


def sync_daily_bars(
    conn,
    *,
    market: str,
    tickers: list[str],
    source: str,
    years: int,
    providers: dict[str, HistoryProvider],
    today: date | None = None,
) -> dict:
    """Fetch + upsert daily bars for every ticker (contract §2). Blocking.

    ``source`` is ``'auto'`` or a concrete provider name already validated
    by the route. ``'auto'`` tries the market's real source first and falls
    back to ``sample`` on failure, annotating the fallback row with the real
    source's error message. Per-ticker failures never abort the batch.
    Commits after each ticker so partial progress survives a crash.

    Returns ``{"results": [{ticker, source, bars, error?}, ...],
    "total_bars": int}`` — rows that persisted bars always carry a positive
    ``bars`` and the source that produced them.
    """
    end = today or datetime.now(timezone.utc).date()
    start = end.fromordinal(max(end.toordinal() - round(years * 365.25), 1))

    if source == "auto":
        chain_names = [REAL_SOURCE_BY_MARKET.get(market, "yfinance"), "sample"]
    else:
        chain_names = [source]
    chain = [(name, providers[name]) for name in chain_names if name in providers]

    results: list[dict] = []
    total_bars = 0
    for raw_ticker in tickers:
        ticker = raw_ticker.strip().upper()
        errors: list[str] = []
        outcome: dict | None = None
        for name, provider in chain:
            try:
                bars = provider.fetch_daily(ticker, start, end)
            except HistoryFetchError as exc:
                errors.append(f"{name}: {exc}")
                continue
            except Exception as exc:  # defensive — providers should wrap
                logger.exception("history provider %s crashed for %s", name, ticker)
                errors.append(f"{name}: {exc}")
                continue
            count = upsert_daily_bars(
                conn, market=market, ticker=ticker, bars=bars, source=name
            )
            conn.commit()
            total_bars += count
            outcome = {"ticker": ticker, "source": name, "bars": count}
            # An auto-mode fallback annotates the real source's failure
            # (contract §2: 失败回落 sample 并在响应标注).
            if errors:
                outcome["error"] = "; ".join(errors)
            break
        if outcome is None:
            outcome = {
                "ticker": ticker,
                "source": chain[-1][0] if chain else source,
                "bars": 0,
                "error": "; ".join(errors) or "no provider available",
            }
        results.append(outcome)
    return {"results": results, "total_bars": total_bars}


def load_recent_daily_bars(conn, market: str, ticker: str, limit: int) -> list[dict]:
    """The most recent ``limit`` stored bars, ASCENDING by date.

    Each row: ``{"date", "open", "high", "low", "close", "volume",
    "source"}`` — the history backtest engine and the daily endpoint both
    read this shape.
    """
    rows = conn.execute(
        "SELECT date, open, high, low, close, volume, source FROM daily_bars "
        "WHERE market = ? AND ticker = ? ORDER BY date DESC LIMIT ?",
        (market, ticker, limit),
    ).fetchall()
    return [dict(row) for row in reversed(rows)]


def ticker_coverage(conn, market: str, ticker: str) -> dict:
    """Whole-table coverage for one ticker: ``{from, to, count}``."""
    row = conn.execute(
        "SELECT MIN(date) AS min_date, MAX(date) AS max_date, COUNT(*) AS n "
        "FROM daily_bars WHERE market = ? AND ticker = ?",
        (market, ticker),
    ).fetchone()
    return {"from": row["min_date"], "to": row["max_date"], "count": row["n"]}


def coverage_rows(conn, market: str) -> list[dict]:
    """Per-ticker coverage for one market, ascending by ticker (contract §2).

    ``source`` is the source of the ticker's NEWEST stored bar — the same
    convention the daily endpoint reports.
    """
    rows = conn.execute(
        "SELECT ticker, MIN(date) AS min_date, MAX(date) AS max_date, "
        "COUNT(*) AS n, "
        "(SELECT source FROM daily_bars AS d2 "
        " WHERE d2.market = d.market AND d2.ticker = d.ticker "
        " ORDER BY d2.date DESC LIMIT 1) AS source "
        "FROM daily_bars AS d WHERE market = ? GROUP BY ticker ORDER BY ticker",
        (market,),
    ).fetchall()
    return [
        {
            "ticker": row["ticker"],
            "from": row["min_date"],
            "to": row["max_date"],
            "count": row["n"],
            "source": row["source"],
        }
        for row in rows
    ]
