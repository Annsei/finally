"""Real-history data layer for FinAlly (D1 contract §1) — daily bars.

Three ``HistoryProvider`` implementations feed the ``daily_bars`` SQLite
table (PRIMARY KEY ``(market, ticker, date)``, upserted via INSERT OR
REPLACE):

- :class:`SampleProvider` — reads the committed synthetic CSVs under
  ``app/market/sample_bars/{market}/`` (generated by
  ``scripts/gen_sample_bars.py`` from a fixed seed; NOT real market data).
  Always available: no network, no optional dependency.
- :class:`YFinanceProvider` (us) — Yahoo daily bars via the ``yfinance``
  package with ``auto_adjust=True`` (OHLC arrives already scaled by the
  adj-close ratio, the contract's US 复权 convention).
- :class:`AkshareProvider` (cn) — 东财 ``stock_zh_a_hist(symbol,
  period="daily", adjust="qfq")`` (前复权), with 2 backoff retries.

CORE INVARIANT (contract §0): the real providers import their packages
LAZILY inside the fetch call — a missing/broken ``yfinance``/``akshare``
install or an offline machine degrades to a clear
:class:`HistoryFetchError` ("source unavailable") on sync only. Nothing in
the live market loop, SSE, trading, rules, or strategy paths imports this
module's optional dependencies, and importing :mod:`app.market.history`
itself never touches them. Providers accept an injectable ``fetch_fn`` so
tests exercise every path with fakes — the test suite never performs real
network I/O.

Storage helpers (:func:`upsert_daily_bars`, :func:`sync_daily_bars`,
:func:`load_recent_daily_bars`, :func:`coverage_rows`) operate on an open
sqlite3 connection supplied by the caller; only :func:`sync_daily_bars`
commits (per ticker, so partial progress survives a mid-sync failure).
"""

from __future__ import annotations

import csv
import logging
import math
import time
from dataclasses import dataclass
from datetime import date, datetime, timezone
from pathlib import Path
from typing import Callable, Protocol

logger = logging.getLogger(__name__)

SAMPLE_BARS_DIR = Path(__file__).parent / "sample_bars"

# Approximate trading days per calendar year — used by SampleProvider to map
# a requested (start, end) window onto its fixed-date synthetic series.
TRADING_DAYS_PER_YEAR = 252

# Real (network) provider per market — 'auto' resolves through this map and
# falls back to the sample provider when the real source fails (contract §2).
REAL_SOURCE_BY_MARKET: dict[str, str] = {"us": "yfinance", "cn": "akshare"}


class HistoryFetchError(Exception):
    """A provider could not produce bars — message is user-presentable."""


@dataclass(frozen=True)
class DailyBar:
    """One daily OHLCV bar. ``date`` is an ISO ``YYYY-MM-DD`` string."""

    date: str
    open: float
    high: float
    low: float
    close: float
    volume: float


class HistoryProvider(Protocol):
    """Protocol every history source implements (contract §1)."""

    name: str

    def fetch_daily(self, ticker: str, start: date, end: date) -> list[DailyBar]:
        """Daily bars for ``ticker`` in ``[start, end]``, ascending by date.

        Raises :class:`HistoryFetchError` with a clear, user-presentable
        message on ANY failure (missing dependency, network error, unknown
        ticker, empty result) — never a bare third-party exception.
        """
        ...


# ---------------------------------------------------------------------------
# Sample provider — committed synthetic CSVs, always available
# ---------------------------------------------------------------------------


class SampleProvider:
    """Reads the committed synthetic sample CSVs. Never touches the network.

    The sample series has FIXED dates (it is committed data), so the
    requested ``(start, end)`` window is interpreted as a duration: the
    provider returns the trailing ``(end - start)`` worth of bars
    (~252/year). This keeps ``years=N`` meaningful forever, regardless of
    how far the wall clock drifts past the committed date range.
    """

    name = "sample"

    def __init__(self, market: str, data_dir: Path | None = None) -> None:
        self.market = market
        self._dir = (data_dir or SAMPLE_BARS_DIR) / market

    def fetch_daily(self, ticker: str, start: date, end: date) -> list[DailyBar]:
        symbol = ticker.strip().upper()
        path = self._dir / f"{symbol}.csv"
        if not path.exists():
            raise HistoryFetchError(
                f"No sample data for {symbol} in the '{self.market}' market"
            )
        try:
            with path.open("r", encoding="utf-8", newline="") as fh:
                rows = list(csv.DictReader(fh))
            bars = [
                DailyBar(
                    date=row["date"],
                    open=float(row["open"]),
                    high=float(row["high"]),
                    low=float(row["low"]),
                    close=float(row["close"]),
                    volume=float(row["volume"]),
                )
                for row in rows
            ]
        except (KeyError, TypeError, ValueError, OSError) as exc:
            raise HistoryFetchError(
                f"Sample data for {symbol} is unreadable: {exc}"
            ) from exc
        if not bars:
            raise HistoryFetchError(f"Sample data for {symbol} is empty")
        span_days = max((end - start).days, 1)
        n = max(1, math.ceil(span_days * TRADING_DAYS_PER_YEAR / 365.25))
        return bars[-n:]


