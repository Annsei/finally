# yfinance provider (us) — lazy import, auto_adjust
# ---------------------------------------------------------------------------


def _import_yfinance():
    """Lazy yfinance import — module-level hook so tests can monkeypatch."""
    import yfinance  # noqa: PLC0415 — deliberate lazy import (contract §1)

    return yfinance


class YFinanceProvider:
    """US daily bars from Yahoo via ``yfinance`` (lazy import).

    ``fetch_fn(ticker, start, end) -> list[DailyBar]`` is injectable for
    tests; the default fetch imports yfinance INSIDE the call and downloads
    with ``auto_adjust=True`` so OHLC comes adj-close scaled (contract §1).
    """

    name = "yfinance"

    def __init__(
        self,
        fetch_fn: Callable[[str, date, date], list[DailyBar]] | None = None,
    ) -> None:
        self._fetch_fn = fetch_fn

    def fetch_daily(self, ticker: str, start: date, end: date) -> list[DailyBar]:
        symbol = ticker.strip().upper()
        fetch = self._fetch_fn or self._default_fetch
        try:
            bars = fetch(symbol, start, end)
        except HistoryFetchError:
            raise
        except Exception as exc:  # third-party errors -> clear message
            raise HistoryFetchError(
                f"yfinance fetch for {symbol} failed: {exc}"
            ) from exc
        if not bars:
            raise HistoryFetchError(
                f"yfinance returned no daily bars for {symbol} — check the "
                "ticker symbol and your network connection"
            )
        return bars

    def _default_fetch(self, symbol: str, start: date, end: date) -> list[DailyBar]:
        try:
            yfinance = _import_yfinance()
        except Exception as exc:
            raise HistoryFetchError(
                "History source 'yfinance' is unavailable — install the "
                f"'yfinance' package to sync US daily bars ({exc})"
            ) from exc
        frame = yfinance.Ticker(symbol).history(
            start=start.isoformat(),
            end=end.isoformat(),
            interval="1d",
            auto_adjust=True,
        )
        bars: list[DailyBar] = []
        for ts, row in frame.iterrows():
            values = [row.get(k) for k in ("Open", "High", "Low", "Close")]
            if any(v is None or v != v for v in values):  # skip NaN rows
                continue
            volume = row.get("Volume")
            bars.append(
                DailyBar(
                    date=ts.date().isoformat(),
                    open=float(values[0]),
                    high=float(values[1]),
                    low=float(values[2]),
                    close=float(values[3]),
                    volume=float(volume) if volume == volume and volume is not None else 0.0,
                )
            )
        bars.sort(key=lambda b: b.date)
        return bars


